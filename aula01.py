# Manipulação de Variáveis e constantes

# - Por meio de variáveis que um algoritmo guardan os dados
# - Todo dado tem a possivilidade de ser alterado no decorrer do tempo deverá ser tratado como uma variável.
# - Quando um dado não tem possibilidade de variar no decorrer do tempo, deverá ser tratado como constante.

a = 10
print(a)

x = 10
x = x + 1
print(x)
